# repeat the tree pattern on the right 
def rep(s,n):
    return s*n

def rep_row(tbl,n):
    tbl_out = []
    for (i,r) in enumerate(tbl):
        tbl_out.append(rep(r,n))
    return tbl_out

# debug 
def gprint(grid):
    for r in grid:
        print(r)
        
# navigation functions
def next(i,j):
    return i+1,j+3

def start():
    return 0,0

def open_or_tree(grid,i,j):
    if grid[i][j] == '.':
        res = 'O'
    if grid[i][j] == '#':
        res = 'X'
    return res
            
def walk(grid):
    result = grid.copy()
    countX = 0
    i,j = start()
    row = 1
    while (row <= len(grid)):
        print('on row {}: ({},{})={}'.format(row,i,j,grid[i][j]))
        r = open_or_tree(grid,i,j)
        if (r == 'X'):
            countX = countX + 1
        # next 
        i,j = next(i,j)
        row = row + 1
    return countX

